    <h2>Présentation</h2>

    <p class="presentation-text">
        Bienvenue sur notre site, nous allons vous présenter la Démarche NIRD : pour un numérique libre et écocitoyen dans les établissements scolaires !
        Un collectif enseignant invite les établissements scolaires et les collectivités qui les accompagnent à s’engager progressivement vers un Numérique qui soit davantage Inclusif, Responsable et Durable, en rejoignant la « démarche NIRD  ».
        Elle se repose sur trois piliers l’inclusion qui est un accès équitable au numérique et une réduction de la fracture numérique, la responsabilité qui est un usage raisonné et réflexif de technologies souveraines et respectueuses des données personnelles et la durabilité qui lutte contre l’obsolescence programmée par le choix de Linux pour l’équipement, maîtrise des coûts…
        Pour plus d’info aller sur <a href="https://nird.forge.apps.education.fr/index.html">nird.forge.apps.education.fr</a>
